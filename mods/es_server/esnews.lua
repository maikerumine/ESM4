--pagenumber = 49
pagenumber = 97

-- register es news nodes

for i = 1, pagenumber do

	local x = i + 1
	local y = i - 1
	if x > pagenumber then x = 1 end
	if y < 1 then y = pagenumber end

	minetest.register_node("es_server:es_news_"..i, {
		description = "ES News "..i,
		drawtype = "signlike",
	    tiles = {"es_news_"..i.."_page.png"},
	    inventory_image = "es_news_"..i.."_page.png",
	    wield_image = "es_news_"..i.."_page.png",
	    paramtype = "light",
	    paramtype2 = "wallmounted",
	    walkable = false,
	    --climbable = true,
		sunlight_propagates = true,
		selection_box = {
			type = "wallmounted",
		},
		groups = {snappy=1, oddly_breakable_by_hand=2, flammable=3},
		sounds = default.node_sound_leaves_defaults(),
		drop = "es_server:es_news_"..i,
		--[[]]
		on_rightclick = function(pos, node, clicker)
			node.name = "es_server:es_news_"..x
			minetest.set_node(pos, node)
			--if math.random(1, 3) == 1 then
				minetest.sound_play("item_drop_pickup.1", {pos = pos})
			--end
		end,
		on_punch = function(pos, node, puncher)
			node.name = "es_server:es_news_"..y
			minetest.set_node(pos, node)
			--if math.random(1, 3) == 1 then
				minetest.sound_play("item_drop_pickup.2", {pos = pos})
			--end
		end,--[[]]
	})

end

